public class PatternN_1 {
    public static void main(String[] args) {
        int count = 1;
        int num= 1;
        for(int i=1; i<=5;i++){
            for(int j=1; j<=num; j++){
                System.out.print(count);
                count++;
            }
              num++;
              System.out.println();
        }
    }
    
}
